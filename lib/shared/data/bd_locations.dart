  /// Backward-compatible alias for callers still using the old name.
  static const Map<String, List<String>> thanas = upazilas;

  /// Get districts for a division.
  static List<String> getDistricts(String division) {
    return districts[division] ?? const <String>[];
  }

  /// Get upazilas for a district.
  static List<String> getUpazilas(String district) {
    return upazilas[district] ?? const <String>[];
  }

  /// Get upazilas for a district using the legacy API name.
  static List<String> getThanas(String district) {
    return getUpazilas(district);
  }

  static List<String> getAllDistricts() {
    final items = <String>[];
    for (final division in divisions) {
      items.addAll(getDistricts(division));
    }
    return items;
  }

  /// Whether the district has a preset upazila list in the app bundle.
  static bool hasPresetThanas(String district) {
    final items = upazilas[district];
    return items != null && items.isNotEmpty;
  }
}
